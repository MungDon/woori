package day0307;

import java.util.Scanner;

public class No_6 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		char inputC;
		
		while (true) {
			String input = sc.nextLine().toUpperCase();
			for (int i = 0; i < input.length(); i++) {
				inputC = input.charAt(i);
			}
		}
	}

}
